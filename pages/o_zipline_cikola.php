        <h2>Proizvodi i usluge</h2>
        <p><strong>Zipline</strong> - Razgledajte krajolik kanjona Čikole iz sasvim novog kuta nad krošnjama stabala i klisura s vjetrom u licu. Zipline je zabavna i uzbudljiva avantura koja omogućuje izvorni doživljaj iz ptičje perspektive pomoću sigurnosne opreme.</p>
        <p><strong>Penjanje po stijenama</strong> - Ljubitelji sportskog penjanja mogu uživati u penjanju po strmim stijenama kanjona rijeke Čikole. Na tim mjestima postoje oznake za sportsko penjanje. Na pojedinim područjima stijene su visoko uzdignute. Odlučite li se na penjačku avanturu uživati ćete u pogledu na klisure.</p>
        <p><strong>Kanjoning</strong> - Kanjon Čikole je izuzetno atraktivan. Zimi kanjonom teče krška rijeka Čikola, a ljeti je kanjon suh i pruža brojne mogućnosti za pješačenje. Pješačka staza duga je 2600 m. U kanjonu Čikole postoji više špilja, od kojih je poznatija "Topla peć". U špiljama su pronađeni ostatci čovjeka iz mlađeg paleolitika, oko 18000 g.pr.Krista.</p>

