        <h3>Čikola</h3>
        <p>Lokalitet kanjona Čikole zaštićen je od 1965. godine. Čikola je najveći pritok rijeke Krke, izvire kod sela Čavoglave, protječe kroz grad Drniš i ulijeva se u rijeku Krku kod jezera Torak. Zipline se nalazi u zaštićenom značajnom krajobrazu rijeke Čikole. Vodeni tok odlikuje se dubokim kanjonom, koji je izniman geomorfološki fenomen, a lokalitet je iznimno važan i kao hidrološki fenomen u kršu.</p>

